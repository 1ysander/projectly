-- Use Edge Functions with service role key to manage/sync integrations.
drop policy if exists integration_accounts_no_direct_access on public.integration_accounts;
create policy integration_accounts_no_direct_access
on public.integration_accounts
for all
to authenticated, anon
using (false)
with check (false);

